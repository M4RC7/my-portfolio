public class p004_multiple_variables{
  public static void main(String[] args){
    //many variables of same type in one line
    int x = 5, y = 6, z = 50;
    System.out.println(x + y + z);
    
    //one value for multiple variables
    int w, v, u;
    w = v = u = 50;
    System.out.println(w + v + u);
  }
}
