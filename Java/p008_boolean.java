public class p008_boolean{
  public static void main(String[] args){
    //raw boolean
    boolean fact = true;
    boolean fake = false;    
    System.out.println(fact);
    System.out.println(fake);
    
    //conditional testing
    System.out.println(10 > 9);
    System.out.println(10 != 10);
    System.out.println(10 == 15);
  }
}
